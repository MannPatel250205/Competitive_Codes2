void aa_rahyo_code(){
    int x, y, k;

    cin >> x >> y >> k;

    if (y <= x)
    {
        cout << x << '\n';
        return;
    }
    
    if (y - x <= k)
    {
        cout << y << '\n';
        return;
    }
    
    cout << x + k + (2 * (y - x - k)) << '\n';;
}

